import React, { useState } from "react";
import { View, Text, TouchableOpacity, StyleSheet, Image } from "react-native";
import { useOnboardingStore } from "../store";
import type { NativeStackScreenProps } from "@react-navigation/native-stack";
import type { RootStackParamList } from "../../types/navigation";
import type { HandSide } from "../../types";

type Props = NativeStackScreenProps<RootStackParamList, "Onboarding">;

type Step = "welcome" | "disclaimer" | "hand" | "permission";

export function OnboardingScreen({ navigation }: Props) {
  const [step, setStep] = useState<Step>("welcome");
  const setHandChoice = useOnboardingStore((s) => s.setHandChoice);
  const setCompleted = useOnboardingStore((s) => s.setCompleted);

  const handleNext = () => {
    if (step === "welcome") setStep("disclaimer");
    else if (step === "disclaimer") setStep("hand");
    else if (step === "hand") setStep("permission");
    else {
      setCompleted(true);
      navigation.replace("Main");
    }
  };

  const handleHandChoice = (side: HandSide, dominant: boolean) => {
    setHandChoice(side, dominant);
    setStep("permission");
  };

  return (
    <View style={styles.container}>
      {step === "welcome" && (
        <>
          {/* App Icon */}
          <Image
            source={require("../../../assets/icon.png")}
            style={styles.icon}
            resizeMode="contain"
          />
          
          <Text style={styles.title} accessibilityRole="header">
            Welcome to AI Palm Reading
          </Text>
          <Text style={styles.description}>
            Discover insights from your palm through ancient palmistry powered by modern AI.
            For reflection and curiosity.
          </Text>
          <TouchableOpacity
            style={styles.button}
            onPress={handleNext}
            accessibilityLabel="Continue to disclaimer"
            accessibilityRole="button"
          >
            <Text style={styles.buttonText}>Get Started</Text>
          </TouchableOpacity>
        </>
      )}
      
      {step === "disclaimer" && (
        <>
          <Text style={styles.emoji}>‚ö†Ô∏è</Text>
          <Text style={styles.title} accessibilityRole="header">
            Disclaimer
          </Text>
          <Text style={styles.description}>
            This app is for entertainment and self-reflection. It does not predict the future. 
            Insights are interpretive and for fun. Results are generated by AI and should be 
            taken as creative interpretations only.
          </Text>
          <TouchableOpacity
            style={styles.button}
            onPress={handleNext}
            accessibilityLabel="I understand, continue"
            accessibilityRole="button"
          >
            <Text style={styles.buttonText}>I Understand</Text>
          </TouchableOpacity>
        </>
      )}
      
      {step === "hand" && (
        <>
          <Text style={styles.emoji}>‚úã</Text>
          <Text style={styles.title} accessibilityRole="header">
            Which hand would you like to read?
          </Text>
          <Text style={styles.description}>
            Choose your dominant hand (the one you write with) for insights about your present and future.
          </Text>
          <View style={styles.handChoices}>
            <TouchableOpacity
              style={styles.handButton}
              onPress={() => handleHandChoice("left", true)}
              accessibilityRole="button"
              accessibilityLabel="Left hand, dominant"
            >
              <Text style={styles.handEmoji}>üñêÔ∏è</Text>
              <Text style={styles.handText}>Left Hand</Text>
              <Text style={styles.handSubtext}>Dominant</Text>
            </TouchableOpacity>
            <TouchableOpacity
              style={styles.handButton}
              onPress={() => handleHandChoice("right", true)}
              accessibilityRole="button"
              accessibilityLabel="Right hand, dominant"
            >
              <Text style={styles.handEmoji}>ü§ö</Text>
              <Text style={styles.handText}>Right Hand</Text>
              <Text style={styles.handSubtext}>Dominant</Text>
            </TouchableOpacity>
          </View>
          <TouchableOpacity style={styles.skipButton} onPress={handleNext}>
            <Text style={styles.skipText}>Skip for now</Text>
          </TouchableOpacity>
        </>
      )}
      
      {step === "permission" && (
        <>
          <Text style={styles.emoji}>üì∏</Text>
          <Text style={styles.title} accessibilityRole="header">
            Camera Access
          </Text>
          <Text style={styles.description}>
            We need camera access to capture your palm photo. Your photo is analyzed by AI 
            and stored locally on your device only. We never upload your images to any server.
          </Text>
          <TouchableOpacity
            style={styles.button}
            onPress={handleNext}
            accessibilityLabel="Continue to app"
            accessibilityRole="button"
          >
            <Text style={styles.buttonText}>Continue</Text>
          </TouchableOpacity>
        </>
      )}
      
      {/* Progress indicator */}
      <View style={styles.progressContainer}>
        <View style={[styles.progressDot, step === "welcome" && styles.progressDotActive]} />
        <View style={[styles.progressDot, step === "disclaimer" && styles.progressDotActive]} />
        <View style={[styles.progressDot, step === "hand" && styles.progressDotActive]} />
        <View style={[styles.progressDot, step === "permission" && styles.progressDotActive]} />
      </View>
    </View>
  );
}

